import streamlit as st
import pandas as pd
import matplotlib.pyplot as plt

st.title("ðŸ“Š Student Performance Analyzer")

uploaded_file = st.file_uploader("Upload Student CSV File", type=["csv"])

if uploaded_file is not None:
    df = pd.read_csv(uploaded_file)

    st.subheader("ðŸ“„ Student Data")
    st.dataframe(df)

    st.subheader("ðŸ“ˆ Statistics")
    st.write(df.describe())

    st.subheader("ðŸŽ¯ Filter Students by Total Marks")
    min_marks = st.slider("Select minimum total marks", 0, 300, 150)

    filtered_df = df[df["Total"] >= min_marks]
    st.dataframe(filtered_df)

    st.subheader("ðŸ“Š Total Marks Chart")
    fig, ax = plt.subplots()
    ax.bar(filtered_df["Name"], filtered_df["Total"])
    ax.set_xlabel("Student Name")
    ax.set_ylabel("Total Marks")
    ax.set_title("Student Total Marks")

    st.pyplot(fig)
else:
    st.info("ðŸ‘† Upload a CSV file to get started")